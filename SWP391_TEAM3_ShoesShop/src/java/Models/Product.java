package Models;

/**
 *
 * @author Admin
 */
public class Product {
    private int productId;
    private String productName;
    private String description;
    private int quantity;
    private double price;
    private int brandId;
    private String avatarP;

    public Product(){}
    public Product(int productId, String productName, String description, int quantity, double price, int brandId, String avatarP) {
        this.productId = productId;
        this.productName = productName;
        this.description = description;
        this.quantity = quantity;
        this.price = price;
        this.brandId = brandId;
        this.avatarP = avatarP;
    }

    public int getProductId() {
        return productId;
    }

    public void setProductId(int productId) {
        this.productId = productId;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getBrandId() {
        return brandId;
    }

    public void setBrandId(int brandId) {
        this.brandId = brandId;
    }

    public String getAvatarP() {
        return avatarP;
    }

    public void setAvatarP(String avatarP) {
        this.avatarP = avatarP;
    }
}